package Model;

public class Conference {
	
		int conferenceId;
		String name;
		
		
		public int getConferenceId() {
			return conferenceId;
		}
		public void setConferenceId(int conferenceId) {
			this.conferenceId = conferenceId;
		}
		public String getName() {
			return name;
		}
		public void setName(String name) {
			this.name = name;
		}
		

}

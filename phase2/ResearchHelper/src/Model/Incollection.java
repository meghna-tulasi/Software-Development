package Model;

public class Incollection {
	int incollectionID;
	String yearID;
	int seriesID;
	String Title;
	public int getIncollectionID() {
		return incollectionID;
	}
	public void setIncollectionID(int incollectionID) {
		this.incollectionID = incollectionID;
	}
	public String getYearID() {
		return yearID;
	}
	public void setYearID(String yearID) {
		this.yearID = yearID;
	}
	public int getSeriesID() {
		return seriesID;
	}
	public void setSeriesID(int seriesID) {
		this.seriesID = seriesID;
	}
	public String getTitle() {
		return Title;
	}
	public void setTitle(String title) {
		Title = title;
	}

}
